import json
import os
from typing import List, Dict, Any
from openai import AsyncOpenAI

# Инициализация асинхронного клиента OpenAI через OpenRouter
client = AsyncOpenAI(
    api_key=os.getenv("OPENAI_API_KEY"),
    base_url="https://openrouter.ai/api/v1"  # Перенаправляем запросы на OpenRouter
)

SYSTEM_PROMPT = """
Ты — Анна, профессиональный и невероятно дружелюбный AI-стилист. 
Твоя главная цель — провести легкий диалог с пользователем, узнать его предпочтения и собрать необходимые данные для поиска одежды.

ЧЕК-ЛИСТ ДАННЫХ ДЛЯ ПОИСКА:
1. Предмет одежды/обуви (что именно ищем: "кроссовки", "вечернее платье", "куртка на весну").
2. Стиль / Повод (casual, оверсайз, деловой, свадьба и т.д.).
3. Бюджет (максимальная сумма, например, "до 15 000 руб").
4. Размер или параметры.

ПРАВИЛА ОБЩЕНИЯ И ВЕДЕНИЯ ДИАЛОГА:
- Общайся на "ты", тепло, эстетично, с легким чувством стиля.
- Задавай НЕ БОЛЕЕ 1-2 вопросов за одно сообщение, чтобы не загружать собеседника.
- Если пользователь прислал ФОТО: внимательно проанализируй его (фасон, цвет, стиль) и сделай комплимент или стильный комментарий, связывая его с текущей задачей.
- Если пользователь задает ОТВЛЕЧЕННЫЕ вопросы (погода, настроение, котики): дай краткий вежливый ответ (1 предложение) и сразу ПЛАВНО возвращай разговор к чек-листу.
- Не затягивай! Как только ключевые параметры (предмет, стиль, бюджет) собраны, подводи итог диалога.
"""

async def run_stylist_agent(messages_history: List[Dict[str, Any]]) -> str:
    """
    Основная функция вызова GPT-4o через OpenRouter API.
    Принимает историю диалога и возвращает текстовый ответ Анны.
    """
    # Собираем массив сообщений с системным промптом во главе
    formatted_messages = [
        {"role": "system", "content": SYSTEM_PROMPT}
    ] + messages_history

    try:
        response = await client.chat.completions.create(
            model="openai/gpt-4o",  # Полный идентификатор модели для OpenRouter
            messages=formatted_messages,
            temperature=0.7,
            max_tokens=600
        )
        
        return response.choices[0].message.content

    except Exception as e:
        print(f"Error calling OpenAI API via OpenRouter: {e}")
        return "Ой, кажется, у меня возникла небольшая техническая заминка. Давай повторим через секунду?"